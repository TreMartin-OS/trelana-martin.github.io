////////////////////////////////////////////////////////////////////////////////

function range(start, end, step = 1) {
  let rArr = [];

  if (step < 0) {
    if (start < end) {
      for (let x = start; x <= end; x += step) {
        break;
      }
    } else if (start > end) {
      for (let x = start; x >= end; x += step) {
        rArr.push(x);
      }
    }
  } else if (step > 0) {
    if (start < end) {
      for (let x = start; x <= end; x += step) {
        rArr.push(x);
      }
    } else if (start > end) {
      for (let x = start; x >= end; x -= step) {
        rArr.push(x);
      }
    }
  }

  return rArr;
}

